
# Addition Operator -> + 

print("3 + 5")   #Output 1 -> `3 + 5`
print("3" + "5") #Output 2 -> `35`
print(3 + 5)     #Output 3 ->  8
#NOTE ! -> Only texts should be placed in quotes, but when we want 
# to calculate arithmetic operations we shouldn't place in the quotes.

# Subtraction Operator -> - 

# "HELLO" add this text "WORLD" -> HELLOWORLD
# "HELLO" takeaway(subtract) "WORLD" -> can't calculate

print("3 - 5")   #Output 1 -> `3 - 5`
#print("3" - "5") #Output 2 ->  error
print(3 - 5)     #Output 3 ->  -2


# Multiplication Operator -> *
# 5 * 7 
# 5 multiplied by 7
# 5 times 7 #this is the way python reads multiplication

print("3 * 5")     #Output 1 -> `3 * 5`
#print("3" * "5")  #Output 2 -> Since the both sides are text can't calculate
print(3 * "5")     #Output 3 -> 555
print("hello"*3)   #Output 4 -> hellohellohello
print(3 * 5)       #Output 5 ->  15


