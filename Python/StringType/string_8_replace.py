# Replace method takes two string arguments, 
# all occurences  of first argument will
# be replaced by the second argument
# in string. 

s = "java"
print(s.replace("a","u")) # juvu

print(s) # java 


print(s.replace("av","python")) # jpythona

print(s.replace("py","ja")) # java

